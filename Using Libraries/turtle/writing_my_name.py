from turtle import *

pensize(3)
def write_a():
    lt(75)
    fd(100)
    lt(215)
    fd(100)
    bk(50)
    rt(110)
    fd(30)
    home()

write_a()
# goto(100,100)
penleft()
up()

def write_b():
    fd(40)
write_b()

done()
